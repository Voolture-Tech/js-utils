/**
 * Convert milliseconds to seconds
 * @param ms number
 * @returns number
 */
export function msToSeconds(ms: number) {
	return ms / 1000;
}

/**
 * Convert seconds to milliseconds
 * @param seconds number
 * @returns number
 */
export function secondsToMs(seconds: number) {
	return seconds * 1000;
}
